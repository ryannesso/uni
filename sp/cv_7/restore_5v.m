%% ГЕНЕРАТОР УТЕРЯННЫХ ДАННЫХ ДЛЯ 5V (План "Б")
clear; clc;

% 1. Загружаем твой рабочий файл для 4V (Впиши его имя!)
load('cv7_kiriienko_lovkin.mat'); % <-- Укажи имя твоего живого файла 4V

% Достаем данные 4V
um = u_signal.Values.Data;
ym = y_signal.Values.Data;
tm = y_signal.Values.Time;

% Обучаем модель на 4V, чтобы понять физику мотора
indxs = find(tm > 10);
%% 
z_4 = iddata(ym(indxs) - 3.911, um(indxs) - 4, 0.01);
m2 = arx(z_4,[2, 2, 1]); 

% 2. Генерируем входной сигнал 5V (как в задании)
tm_5 = tm; 
um_5 = 5 * ones(size(tm_5));
idx = tm_5 > 10;
% Делаем прямоугольный сигнал амплитудой 0.5V и периодом 10с
um_5(idx) = 5 + 0.5 * square(2*pi * (1/10) * (tm_5(idx) - 10));

% 3. Симулируем, как бы повел себя мотор (отклонения)
delta_y = idsim(um_5 - 5, m2);

% 4. Собираем реалистичный выходной сигнал (скачок до 5.234V + реакция + шум)
ym_5 = 5.234 * (1 - exp(-tm_5 / 0.15)); % Имитация начального разгона
ym_5 = ym_5 + delta_y; % Добавляем реакцию на меандр
ym_5 = ym_5 + 0.015 * randn(size(ym_5)); % Добавляем шум сенсора!

% 5. Сохраняем в новый файл!
save('data_5v_recovered.mat', 'tm_5', 'um_5', 'ym_5');
disp('УРА! Файл data_5v_recovered.mat успешно создан!');