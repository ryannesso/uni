% init_params.m
% Скрипт инициализации параметров для моделей Simulink

clear; clc;

%% 1. Исходные параметры (из Таблицы 1)
S1 = 1;         % Площадь S1, м^2
S2 = 1;         % Площадь S2, м^2

% Перевод площадей отверстий из см^2 в м^2
S10 = 100 / 10000; % 0.01 м^2
S20 = 100 / 10000; % 0.01 м^2

mu = 0.62;      % Коэффициент расхода
g = 9.81;       % Ускорение свободного падения, м/с^2

%% 2. Параметры для Задачи 2 (Симуляция наполнения)
Q1_initial = 0.01942;     % Начальный входной поток, м^3/с
Q1_step_time = 700;       % Время изменения потока, с
Q1_final = Q1_initial / 2; % Конечный поток (половина), м^3/с

%% 3. Вычисляемые константы для блоков Gain в Simulink
% Уравнение потока: Q = mu * S * sqrt(2*g) * sqrt(h)
% Мы считаем константную часть "mu * S * sqrt(2*g)", чтобы в Симулинке вписать одно слово.

K_out1 = mu * S10 * sqrt(2 * g); % Коэффициент для отверстия S10
K_out2 = mu * S20 * sqrt(2 * g); % Коэффициент для отверстия S20

% Обратные площади резервуаров (для блоков Gain перед интегратором)
inv_S1 = 1 / S1;
inv_S2 = 1 / S2;

%% 4. Расчеты для Задачи 4 (Поиск Q1 для установившегося h2 = 0.3 м)
h2_target = 0.3; % Заданная высота, м

% Теоретический Q1 для Системы 1
Q1_target_sys1 = mu * S20 * sqrt(2 * g * h2_target);

% Теоретический Q1 для Системы 2
Q1_target_sys2 = (mu * S10 * sqrt(2) + mu * S20) * sqrt(2 * g * h2_target);

disp('✅ Все параметры успешно загружены в Workspace!');
disp(['Для задачи 4 (Система 1) нужен поток: Q1 = ', num2str(Q1_target_sys1)]);
disp(['Для задачи 4 (Система 2) нужен поток: Q1 = ', num2str(Q1_target_sys2)]);